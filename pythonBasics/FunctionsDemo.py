# In Python, function is a group of related statements that perform a specific task

# Function Declaration
def GreetMe(name):  # def - define a function, GreetMe - function name, () - variables, : - colon needed
    print("Good morning! " + name)

def AddIntegers(c, d):
    return c + d

# Function Call
GreetMe("Emily")
print(AddIntegers(91, 97))
